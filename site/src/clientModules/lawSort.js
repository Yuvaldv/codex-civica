import { GENERATED_LAW_META } from '../generatedLawMeta';

// ─── helpers ────────────────────────────────────────────────────────────────

function getLawId(href) {
  const m = (href || '').match(/\/laws\/(\d+)/);
  return m ? m[1] : null;
}

function getGroup() {
  try { return localStorage.getItem('law-group') || 'year'; } catch { return 'year'; }
}

function setGroup(v) {
  try { localStorage.setItem('law-group', v); } catch {}
}

// ─── group key derivation ───────────────────────────────────────────────────

const STATUS_HE = { 'In Effect': 'תקף', 'Cancelled': 'בטל', 'Expired': 'פקע' };

function getGroupKey(groupBy, meta) {
  switch (groupBy) {
    case 'year':     return meta.year ? String(meta.year) : '?';
    case 'category': return meta.categoryLabelHe || meta.categoryLabel || 'אחר';
    case 'minister': return meta.minister || 'אחר';
    case 'status':   return meta.statusHe || STATUS_HE[meta.status] || meta.status || '?';
    default:         return meta.year ? String(meta.year) : '?';
  }
}

// ─── sidebar grouping ───────────────────────────────────────────────────────

function findLawList() {
  // Autogenerated sidebar: all law items are direct children of the root menu list
  return document.querySelector('ul.theme-doc-sidebar-menu.menu__list');
}

function createGroupElement(label, items) {
  const li = document.createElement('li');
  li.className = 'menu__list-item law-group';

  const wrap = document.createElement('div');
  wrap.className = 'menu__list-item-collapsible';

  const btn = document.createElement('button');
  btn.className = 'menu__link menu__link--sublist menu__link--sublist-caret';
  btn.type = 'button';
  btn.textContent = label;
  btn.addEventListener('click', () => li.classList.toggle('menu__list-item--collapsed'));

  wrap.appendChild(btn);
  li.appendChild(wrap);

  const ul = document.createElement('ul');
  ul.className = 'menu__list';
  for (const item of items) ul.appendChild(item);
  li.appendChild(ul);

  return li;
}

function applyGroup(groupBy) {
  const lawList = findLawList();
  if (!lawList) return;

  // Collect all law <li> items (those with a /laws/<id> link)
  const seen = new Set();
  const lawItems = [];
  for (const a of lawList.querySelectorAll('a[href*="/laws/"]')) {
    const id = getLawId(a.getAttribute('href'));
    if (!id) continue; // skip index page
    const li = a.closest('li.menu__list-item');
    if (li && !seen.has(li)) {
      seen.add(li);
      lawItems.push(li);
    }
  }
  if (lawItems.length === 0) return;

  // Detach law items from wherever they currently live
  for (const item of lawItems) item.parentNode?.removeChild(item);

  // Remove stale law-group containers
  for (const g of lawList.querySelectorAll('li.law-group')) g.remove();

  // Group
  const groups = new Map();
  for (const item of lawItems) {
    const id = getLawId(item.querySelector('a')?.getAttribute('href'));
    const meta = GENERATED_LAW_META[id] || {};
    const key = getGroupKey(groupBy, meta);
    if (!groups.has(key)) groups.set(key, []);
    groups.get(key).push(item);
  }

  // Sort keys: numeric for year, locale for everything else
  const sortedKeys = [...groups.keys()].sort((a, b) =>
    groupBy === 'year' ? Number(a) - Number(b) : a.localeCompare(b, 'he')
  );

  for (const key of sortedKeys) {
    lawList.appendChild(createGroupElement(key, groups.get(key)));
  }
}

// ─── metadata bubbles on law pages ─────────────────────────────────────────

function injectMetaBubbles(pathname) {
  const m = pathname.match(/\/laws\/(\d+)/);
  if (!m) return;
  const id = m[1];
  const meta = GENERATED_LAW_META[id];
  if (!meta) return;

  // Remove any previously injected bar
  document.querySelectorAll('.law-meta-bar').forEach(el => el.remove());

  // Find insertion point: after the first h1 in .markdown
  const h1 = document.querySelector('.markdown > h1');
  if (!h1) return;

  const bar = document.createElement('div');
  bar.className = 'law-meta-bar';

  const addBadge = (text, type) => {
    if (!text) return;
    const s = document.createElement('span');
    s.className = 'law-meta-badge law-meta-badge--' + type;
    s.textContent = text;
    bar.appendChild(s);
  };

  addBadge(meta.categoryLabelHe || meta.categoryLabel, 'category');
  (meta.tags || []).forEach(t => addBadge(t, 'tag'));
  if (meta.statusHe) addBadge(meta.statusHe, 'status-' + (meta.status || '').toLowerCase().replace(/\s+/g, '-'));
  if (meta.year)     addBadge(String(meta.year), 'year');
  if (meta.minister) addBadge(meta.minister, 'minister');

  h1.insertAdjacentElement('afterend', bar);
}

// ─── navbar Group-by visibility ─────────────────────────────────────────────

let _showGroupBy = false;

function syncGroupByVisibility() {
  document.querySelectorAll('[id="navbar-sortby"]').forEach(el => {
    el.style.display = _showGroupBy ? 'flex' : 'none';
  });
}

function syncSelect() {
  const val = getGroup();
  document.querySelectorAll('[id="law-sort-select"]').forEach(sel => { sel.value = val; });
}

function updateVisibility(pathname) {
  _showGroupBy = pathname.includes('/laws');
  syncGroupByVisibility();
}

// ─── Docusaurus lifecycle hooks ─────────────────────────────────────────────

export function onRouteDidUpdate({ location }) {
  updateVisibility(location.pathname);
  syncSelect();
  requestAnimationFrame(() => setTimeout(() => {
    applyGroup(getGroup());
    injectMetaBubbles(location.pathname);
  }, 80));
}

// ─── initial page load ───────────────────────────────────────────────────────

if (typeof window !== 'undefined') {
  window.addEventListener('DOMContentLoaded', () => {
    updateVisibility(window.location.pathname);
    syncSelect();

    document.addEventListener('change', e => {
      if (e.target && e.target.id === 'law-sort-select') {
        const val = e.target.value;
        setGroup(val);
        syncSelect();
        applyGroup(val);
      }
    });

    // Re-sync when Docusaurus mounts mobile hamburger
    let rafId = null;
    const obs = new MutationObserver(() => {
      if (rafId) return;
      rafId = requestAnimationFrame(() => { syncGroupByVisibility(); syncSelect(); rafId = null; });
    });
    obs.observe(document.body, { childList: true, subtree: true });

    setTimeout(() => {
      applyGroup(getGroup());
      injectMetaBubbles(window.location.pathname);
    }, 300);
  });
}
